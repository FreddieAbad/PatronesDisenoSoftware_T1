    private void productoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_productoButtonActionPerformed
        if (nombreTextField.getText().equals("")
                || precioTextField.getText().equals("")
                || cantidadTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Colocar un valor para nombre, precio y cantidad.");
        } else {

            String productoString = inventarioController.agregarProducto(nombreTextField.getText(),
                    Double.parseDouble(precioTextField.getText()),
                    Integer.parseInt(cantidadTextField.getText()));

            plantilla.addRow(new Object[]{
                productoString
            });
            String total = Double.toString(inventarioController.calcularValorTotal());
            totalText.setText(total);
            nombreTextField.setText("");
            precioTextField.setText("");
            cantidadTextField.setText("");

        }
    }//GEN-LAST:event_productoButtonActionPerformed

