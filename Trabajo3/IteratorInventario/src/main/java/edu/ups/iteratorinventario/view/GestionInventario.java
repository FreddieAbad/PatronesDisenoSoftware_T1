    private void precioTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_precioTextFieldKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c) && (c != '.' || cantidadTextField.getText().contains("."))) {
            evt.consume();
        }
    }//GEN-LAST:event_precioTextFieldKeyTyped

    private void cantidadTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cantidadTextFieldKeyTyped
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) {
            evt.consume();
        }
    }//GEN-LAST:event_cantidadTextFieldKeyTyped

    private void generarInformeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarInformeButtonActionPerformed
        // TODO add your handling code here:
        List<String> informe = inventarioController.generarInforme();
        StringBuilder sb = new StringBuilder();
        for (String elemento : informe) {
            sb.append(elemento);
            sb.append("\n"); 
        }
        String total = Double.toString(inventarioController.calcularValorTotal());
        String resultado = sb.toString();
        String informeImprimir = "INFORME GENERADO \n Valor Total: "+total+"\n DETALLE:\n"+resultado;
        JOptionPane.showMessageDialog(null, informeImprimir);

    }//GEN-LAST:event_generarInformeButtonActionPerformed

